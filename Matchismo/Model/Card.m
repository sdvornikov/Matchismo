#import "Card.h"

// just a comment to test pull requests
@implementation Card

- (int)match:(NSArray *)otherCards {
    int score = 0;
    for (Card *card in otherCards) {
        if ([self.contents isEqualToString:card.contents]) {
            score = 1;
        }
    }
    return score;
}

- (NSString *)description {
    return self.contents;
}

@end
